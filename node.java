public class node {
    private String name;
    private int distance;

    public node(String name, int distance)
    {
        this.name = name;
        this.distance = distance;
    }

    public int get_distance()
    {
        return distance;
    }

    public void set_distance(int distance)
    {
        this.distance = distance;
    }

    public String get_name()
    {
        return name;
    }




    
}
